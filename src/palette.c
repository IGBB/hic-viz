#define RGBr(code) (((code) >> 16) & 0xFF)
#define RGBg(code) (((code) >> 8) & 0xFF)
#define RGBb(code) ((code) & 0xFF)



int * load_palette(gdImagePtr img, const int pal[] ){
    int i;
    int * colors = malloc( 256 * sizeof(int));

    for(i = 0; i < 256; i++)
        colors[i] = gdImageColorAllocate(img,
                                         RGBr(pal[i]),
                                         RGBg(pal[i]),
                                         RGBb(pal[i]));

   return colors;
}


